function generateResultSummary(results, scores) {
  const tendencyMap = {
    '假性倾向': '从这次初筛看，你目前更偏向假性表现，说明你的左右差异更可能与习惯、姿态或使用方式有关。',
    '混合倾向': '从这次初筛看，你目前更偏向混合表现，说明外观差异里可能同时存在使用习惯与结构稳定因素。',
    '真性倾向': '从这次初筛看，你目前更偏向真性表现，说明你的左右差异相对更稳定，不太容易因为姿态变化而明显减轻。'
  };
  const directionMap = {
    '习惯偏侧方向': '主问题方向更偏向习惯偏侧，通常意味着单侧使用、咀嚼或支撑方式在持续放大左右差异。',
    '肌肉软组织方向': '主问题方向更偏向肌肉软组织，通常表现为一侧更紧、更厚、更顶，外观更容易出现厚薄差。',
    '筋膜位置牵拉方向': '主问题方向更偏向筋膜位置牵拉，通常表现为一侧像被拉偏，不只是厚薄变化。',
    '动态控制方向': '主问题方向更偏向动态控制，说明你在说话、微笑或表情启动时更容易把差异放大。',
    '体态代偿方向': '主问题方向更偏向体态代偿，说明头颈、肩颈和整体支撑关系可能在参与脸部偏侧表现。'
  };
  const sideMap = {
    '左侧更明显': '这次初筛里左侧表现更明显，后续观察和处理时可以优先留意左侧。',
    '右侧更明显': '这次初筛里右侧表现更明显，后续观察和处理时可以优先留意右侧。',
    '暂不明确': '这次初筛里还没有形成明确单侧结论，说明你的情况可能更偏混合或波动。'
  };

  let closing = '这份结果只代表当前免费初筛倾向。';
  if (results.complexity === '建议进一步评估') closing = '你的情况里已经出现复合或动态放大的信号，更适合进入下一步细化分析。';
  else if (scores.dynamicScore >= 4) closing = '你的动态参与度较高，后续更建议结合说话和微笑状态继续观察。';

  return [tendencyMap[results.tendency], directionMap[results.direction], sideMap[results.side], closing].join('');
}
